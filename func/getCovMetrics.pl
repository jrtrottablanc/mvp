# ROI file
#~ if ($experiment eq 'target' && defined($roiType)) {
	#~ my ($ROIFile) = $$conf{$roiType} =~ m/^.*\/target\/(.*\.bed)/; 
	#~ print (HISTSTATS "##ROI BED file: $ROIFile\n");
	#~ print (HISTSTATSJSON "\t\"ROI BED file\":\"$ROIFile\",\n");
#~ }
#~ else {
	#~ print (HISTSTATS "##ROI BED file: NA\n");
	#~ print (HISTSTATSJSON "\t\"ROI BED file\":\"NA\",\n"); }

#~ # Mappability
print (HISTSTATS "##Mappability: 150\n");
print (HISTSTATSJSON "\t\"Mappability\":\"150\",\n");
	
# Count type
print (HISTSTATS "##Count type: $countType\n");
print (HISTSTATSJSON "\t\"Count type\":\"$countType\",\n");

# Count type
print (HISTSTATS "##Discard duplicates: T\n");
print (HISTSTATSJSON "\t\"Discard duplicates\":\"T\",\n");

# BaseQ
print (HISTSTATS "##Minimum base quality: $baseQ\n");
print (HISTSTATSJSON "\t\"Minimum base quality\":\"$baseQ\",\n");
	
# MapQ
print (HISTSTATS "##Minimum mapping quality: $mapQ\n");
print (HISTSTATSJSON "\t\"Minimum mapping quality\":\"$mapQ\",\n");
		
# Header
my $barcodeType = ucfirst($readGroup);
print (HISTSTATS "#$barcodeType\tROI_Size\tMean_cov\tMedian_cov\t");	
foreach my $key (sort {$a <=> $b} keys(%covHash)) {
	print (HISTSTATS "C$key\t"); }
print (HISTSTATS "Evenness\tMedian_Evenness\tSpecificity\tEnrichment\n");
	
# Data
print (HISTSTATS "$prefix\t$size_final\t$mean_cov\t$median_cov\t");
foreach my $key (sort {$a <=> $b} keys(%covHash)) {
	print (HISTSTATS abs($covHash{$key})."\t"); }
print (HISTSTATS "$evenness\t$mevenness\t$specificity\t$enrichment\n");

# Data in json format
print (HISTSTATSJSON "\t\"$barcodeType\":\"$prefix\",\n");
print (HISTSTATSJSON "\t\"ROI_Size\":\"$size_final\",\n");
print (HISTSTATSJSON "\t\"Mean_cov\":\"$mean_cov\",\n");
print (HISTSTATSJSON "\t\"Median_cov\":\"$median_cov\",\n");
foreach my $key (sort {$a <=> $b} keys(%covHash)) {
	print (HISTSTATSJSON "\t\"C$key\":\"".abs($covHash{$key})."\",\n"); }	
print (HISTSTATSJSON "\t\"Evenness\":\"$evenness\",\n");
print (HISTSTATSJSON "\t\"Median_Evenness\":\"$mevenness\",\n");
print (HISTSTATSJSON "\t\"Specificity\":\"$specificity\",\n");
print (HISTSTATSJSON "\t\"Enrichment\":\"$enrichment\"\n");
	
close(HISTSTATS);
print (HISTSTATSJSON "}\n");
close(HISTSTATSJSON);


# SUB FUNCTIONS
sub getEvenness {
	
	my ($cov,$size,$histBaseNumberIndex,$data) = @_;
	my $evenness;
	if ($cov < 1) {
		$evenness = 'NA';	}
	else {
		my $pi = 0;
		for my $i (1 .. $cov) {
			foreach (@{$data}) {
				my @line = split ("\t",$_);
				if ($line[0] >= $i) {
					$pi = $pi + $line[$histBaseNumberIndex]; }
			}
		}
		$evenness = sprintf("%.3f",( ( $pi/($cov*$size) )*100) );
	}
	return $evenness;
}
